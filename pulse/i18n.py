def normalize_language(language: str) -> str:
    text = str(language or "").strip().lower()
    return text if text in SUPPORTED_LANGUAGES else "en"


def tr(language: str, key: str, **kwargs) -> str:
    lang = normalize_language(language)
    text = STRINGS.get(lang, {}).get(key) or STRINGS["en"].get(key) or key
    return text.format(**kwargs) if kwargs else text


def weekday_label(language: str, weekday_index: int, abbreviated: bool = False) -> str:
    lang = normalize_language(language)
    style = "short" if abbreviated else "full"
    labels = WEEKDAY_LABELS.get(lang, WEEKDAY_LABELS["en"])[style]
    return labels[weekday_index % 7]


def month_label(language: str, month_index: int) -> str:
    lang = normalize_language(language)
    labels = MONTH_LABELS.get(lang, MONTH_LABELS["en"])
    return labels[(month_index - 1) % 12]


def day_count_label(language: str, count: int) -> str:
    lang = normalize_language(language)
    if lang == "ru":
        remainder_10 = count % 10
        remainder_100 = count % 100
        if remainder_10 == 1 and remainder_100 != 11:
            return "день"
        if remainder_10 in (2, 3, 4) and remainder_100 not in (12, 13, 14):
            return "дня"
        return "дней"
    return "day" if count == 1 else "days"
